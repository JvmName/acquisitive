val ColorScheme.hotColor: Color
    get() = Color(0xFFF19D38)

@Composable
fun ColorScheme.indent(level: Int): Color = indent(level, isSystemInDarkTheme())

fun ColorScheme.indent(level: Int, isDark: Boolean): Color {
    val indent = level % 10
    return if (!isDark) {
        when (indent) {
            0 -> lightIndent0
            1 -> lightIndent1
            2 -> lightIndent2
            3 -> lightIndent3
            4 -> lightIndent4
            5 -> lightIndent5
            6 -> lightIndent6
            7 -> lightIndent7
            8 -> lightIndent8
            9 -> lightIndent9
            else -> error("impossible color")
        }
    } else {
        when (indent) {
            0 -> darkIndent0
            1 -> darkIndent1
            2 -> darkIndent2
            3 -> darkIndent3
            4 -> darkIndent4
            5 -> darkIndent5
            6 -> darkIndent6
            7 -> darkIndent7
            8 -> darkIndent8
            9 -> darkIndent9
            else -> error("impossible color")
        }
    }
}